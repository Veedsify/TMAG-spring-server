INSERT INTO roles (created_at, updated_at, name, permissions)
SELECT CURRENT_TIMESTAMP, CURRENT_TIMESTAMP, 'Affiliate', '[]'
WHERE NOT EXISTS (SELECT 1 FROM roles WHERE name = 'Affiliate');

INSERT INTO permissions (created_at, updated_at, name, description, resource_type, action)
SELECT CURRENT_TIMESTAMP, CURRENT_TIMESTAMP, 'affiliate:' || actions.action_value, 'Can ' || actions.action_value || ' affiliate', 'affiliate', actions.action_value
FROM (VALUES ('create'), ('read'), ('update'), ('delete'), ('list')) AS actions(action_value)
WHERE NOT EXISTS (SELECT 1 FROM permissions p WHERE p.name = 'affiliate:' || actions.action_value);

INSERT INTO role_permissions (created_at, updated_at, role_id, permission_id)
SELECT CURRENT_TIMESTAMP, CURRENT_TIMESTAMP, r.id, p.id
FROM roles r
JOIN permissions p ON p.name IN ('affiliate:create', 'affiliate:read', 'affiliate:update', 'affiliate:list', 'profile:read', 'profile:update', 'pricing_plan:read', 'pricing_plan:list')
WHERE r.name = 'Affiliate'
  AND NOT EXISTS (
      SELECT 1 FROM role_permissions rp WHERE rp.role_id = r.id AND rp.permission_id = p.id
  );

INSERT INTO role_permissions (created_at, updated_at, role_id, permission_id)
SELECT CURRENT_TIMESTAMP, CURRENT_TIMESTAMP, r.id, p.id
FROM roles r
JOIN permissions p ON p.name LIKE 'affiliate:%'
WHERE r.name IN ('SuperAdmin', 'Administrator')
  AND NOT EXISTS (
      SELECT 1 FROM role_permissions rp WHERE rp.role_id = r.id AND rp.permission_id = p.id
  );
