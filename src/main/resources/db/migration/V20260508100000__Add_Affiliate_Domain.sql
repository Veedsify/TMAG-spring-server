CREATE TABLE IF NOT EXISTS affiliate_profiles
(
    id                      BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    created_at              TIMESTAMP WITHOUT TIME ZONE,
    updated_at              TIMESTAMP WITHOUT TIME ZONE,
    deleted_at              TIMESTAMP WITHOUT TIME ZONE,
    user_id                 BIGINT                                  NOT NULL,
    referral_code           VARCHAR(40)                             NOT NULL,
    commission_rate         DECIMAL(5, 2)                           NOT NULL DEFAULT 10.00,
    discount_rate           DECIMAL(5, 2)                           NOT NULL DEFAULT 2.00,
    total_clicks            INTEGER                                 NOT NULL DEFAULT 0,
    total_conversions       INTEGER                                 NOT NULL DEFAULT 0,
    total_commission_earned DECIMAL(12, 2)                          NOT NULL DEFAULT 0.00,
    total_paid_out          DECIMAL(12, 2)                          NOT NULL DEFAULT 0.00,
    pending_commission      DECIMAL(12, 2)                          NOT NULL DEFAULT 0.00,
    status                  VARCHAR(20)                             NOT NULL DEFAULT 'active',
    CONSTRAINT pk_affiliate_profiles PRIMARY KEY (id),
    CONSTRAINT uk_affiliate_profiles_user UNIQUE (user_id),
    CONSTRAINT uk_affiliate_profiles_referral_code UNIQUE (referral_code),
    CONSTRAINT fk_affiliate_profiles_user FOREIGN KEY (user_id) REFERENCES users (id)
);

CREATE TABLE IF NOT EXISTS affiliate_referral_links
(
    id                BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    created_at        TIMESTAMP WITHOUT TIME ZONE,
    updated_at        TIMESTAMP WITHOUT TIME ZONE,
    deleted_at        TIMESTAMP WITHOUT TIME ZONE,
    affiliate_id      BIGINT                                  NOT NULL,
    credit_plan_id    BIGINT,
    campaign          VARCHAR(120)                            NOT NULL,
    destination_url   TEXT                                    NOT NULL,
    short_code        VARCHAR(40)                             NOT NULL,
    clicks            INTEGER                                 NOT NULL DEFAULT 0,
    conversions       INTEGER                                 NOT NULL DEFAULT 0,
    commission_earned DECIMAL(12, 2)                          NOT NULL DEFAULT 0.00,
    is_active         BOOLEAN                                 NOT NULL DEFAULT TRUE,
    CONSTRAINT pk_affiliate_referral_links PRIMARY KEY (id),
    CONSTRAINT uk_affiliate_referral_links_short_code UNIQUE (short_code),
    CONSTRAINT fk_affiliate_referral_links_affiliate FOREIGN KEY (affiliate_id) REFERENCES affiliate_profiles (id),
    CONSTRAINT fk_affiliate_referral_links_credit_plan FOREIGN KEY (credit_plan_id) REFERENCES user_credit_plans (id)
);

CREATE TABLE IF NOT EXISTS affiliate_referrals
(
    id               BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    created_at       TIMESTAMP WITHOUT TIME ZONE,
    updated_at       TIMESTAMP WITHOUT TIME ZONE,
    deleted_at       TIMESTAMP WITHOUT TIME ZONE,
    affiliate_id     BIGINT                                  NOT NULL,
    referral_link_id BIGINT,
    referred_user_id BIGINT                                  NOT NULL,
    referral_code    VARCHAR(40)                             NOT NULL,
    status           VARCHAR(20)                             NOT NULL DEFAULT 'active',
    first_click_at   TIMESTAMP WITHOUT TIME ZONE,
    converted_at     TIMESTAMP WITHOUT TIME ZONE,
    CONSTRAINT pk_affiliate_referrals PRIMARY KEY (id),
    CONSTRAINT uk_affiliate_referrals_referred_user UNIQUE (referred_user_id),
    CONSTRAINT fk_affiliate_referrals_affiliate FOREIGN KEY (affiliate_id) REFERENCES affiliate_profiles (id),
    CONSTRAINT fk_affiliate_referrals_link FOREIGN KEY (referral_link_id) REFERENCES affiliate_referral_links (id),
    CONSTRAINT fk_affiliate_referrals_user FOREIGN KEY (referred_user_id) REFERENCES users (id)
);

CREATE TABLE IF NOT EXISTS affiliate_commissions
(
    id               BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    created_at       TIMESTAMP WITHOUT TIME ZONE,
    updated_at       TIMESTAMP WITHOUT TIME ZONE,
    deleted_at       TIMESTAMP WITHOUT TIME ZONE,
    affiliate_id     BIGINT                                  NOT NULL,
    referral_link_id BIGINT,
    referred_user_id BIGINT,
    amount           DECIMAL(12, 2)                          NOT NULL,
    base_amount      DECIMAL(12, 2)                          NOT NULL,
    rate             DECIMAL(5, 2)                           NOT NULL,
    status           VARCHAR(20)                             NOT NULL DEFAULT 'approved',
    customer_email   VARCHAR(255),
    reference_type   VARCHAR(60)                             NOT NULL,
    reference_id     BIGINT                                  NOT NULL,
    paid_at          TIMESTAMP WITHOUT TIME ZONE,
    CONSTRAINT pk_affiliate_commissions PRIMARY KEY (id),
    CONSTRAINT fk_affiliate_commissions_affiliate FOREIGN KEY (affiliate_id) REFERENCES affiliate_profiles (id),
    CONSTRAINT fk_affiliate_commissions_link FOREIGN KEY (referral_link_id) REFERENCES affiliate_referral_links (id),
    CONSTRAINT fk_affiliate_commissions_user FOREIGN KEY (referred_user_id) REFERENCES users (id)
);

CREATE TABLE IF NOT EXISTS affiliate_payouts
(
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    created_at      TIMESTAMP WITHOUT TIME ZONE,
    updated_at      TIMESTAMP WITHOUT TIME ZONE,
    deleted_at      TIMESTAMP WITHOUT TIME ZONE,
    affiliate_id    BIGINT                                  NOT NULL,
    amount          DECIMAL(12, 2)                          NOT NULL,
    payment_method  VARCHAR(80)                             NOT NULL,
    payment_details TEXT                                    NOT NULL,
    status          VARCHAR(20)                             NOT NULL DEFAULT 'pending',
    notes           TEXT,
    requested_at    TIMESTAMP WITHOUT TIME ZONE             NOT NULL DEFAULT CURRENT_TIMESTAMP,
    processed_at    TIMESTAMP WITHOUT TIME ZONE,
